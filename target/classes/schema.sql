CREATE TABLE IF NOT EXISTS publisher (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(255),
    location VARCHAR(255)
);

CREATE TABLE IF NOT EXISTS book (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    title VARCHAR(255),
    author VARCHAR(255),
    pages INT,
    publisher_id BIGINT,
    FOREIGN KEY (publisher_id) REFERENCES publisher(id)
);
